import Foundation
import UIKit

extension UIColor {

    class func flk_logoBlueColor() -> UIColor {
        return UIColor(red: 24/255.0, green: 109/255.0, blue: 238/255.0, alpha: 1.0)
    }

    class func flk_logoRedColor() -> UIColor {
        return UIColor(red: 219/255.0, green: 71/255.0, blue: 51/255.0, alpha: 1.0)
    }

    class func flk_logoYellowColor() -> UIColor {
        return UIColor(red: 255/255.0, green: 186/255.0, blue: 3/255.0, alpha: 1.0)
    }

    class func flk_logoGreenColor() -> UIColor {
        return UIColor(red: 0/255.0, green: 153/255.0, blue: 85/255.0, alpha: 1.0)
    }

}